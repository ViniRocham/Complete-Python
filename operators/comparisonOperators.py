
#     == Equal
#     != Not equal
#     >  Greater than
#     <  Less than
#     >= Greater than or equal to
#     <= Less than or equal to


# ==
operators = 10 == 10
operators1 = 'banana' == 'Banana'
print(operators)
print(operators1)

# !=
operators2 = 'watermeloon' != 'apple'
print(operators2)

# >
operators3 = 3 > 5
print(operators3)

# <
operators4 = 3 < 5
print(operators4)

# >=
operators5 = 3 >= 5
print(operators5)

# <=
operators6 = 3 <= 5
print(operators6)